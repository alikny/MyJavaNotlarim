package review_sessions.week4;



public class SumOfNumbers {

	public static void main(String[] args) {
		
		//sum of all numbers between 10 - 30
		
		
		int sum = 0;
		int counter = 0;
		
		
		
		for(int i=10;i<=30;i++) {
			
			sum = sum + i;  
			// 0+10 =11
			// 11+12 = 23
			counter++;
			
			
			
			
		}
		
		System.out.println(sum);
		System.out.println(counter);

	}
	

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	


}
